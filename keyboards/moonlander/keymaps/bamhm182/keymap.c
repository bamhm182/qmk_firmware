/* Key Presses */

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch(keycode) {
        case MK_RST:
            if (record->event.pressed) {
                PLAY_SONG(upgrade_song);
                SEND_STRING(SS_DELAY(800));
                reset_keyboard();
            }
            break;
    }
    return true;
};

